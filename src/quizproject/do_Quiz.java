    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if (i + 1 > arr.size() - 1) {
            JOptionPane.showMessageDialog(this, "You have Reached The Last Qustion");
            return;
        }
        i++;
        jLabel3.setText(String.valueOf((i + 1) + " / " + arr.size()));
        jTextField1.setText(arr.get(i).getQ());
        jTextField2.setText(arr.get(i).getA());
        jTextField3.setText(arr.get(i).getB());
        jTextField4.setText(arr.get(i).getC());
        jTextField5.setText(arr.get(i).getD());
        corret = arr.get(i).getCorrect();
        if (!questions.contains(arr.get(i).getQ())) {
            jRadioButton1.setEnabled(true);
            jRadioButton2.setEnabled(true);
            jRadioButton3.setEnabled(true);
            jRadioButton4.setEnabled(true);
            jButton2.setEnabled(true);
        } else {
            jRadioButton1.setEnabled(false);
            jRadioButton2.setEnabled(false);
            jRadioButton3.setEnabled(false);
            jRadioButton4.setEnabled(false);
            jButton2.setEnabled(false);
        }

    }//GEN-LAST:event_jButton1ActionPerformed

