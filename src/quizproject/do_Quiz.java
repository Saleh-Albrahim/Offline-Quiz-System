    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        right = false;
        if (jRadioButton1.isSelected()) {
            if (jRadioButton1.getText().equals(corret)) {
                JOptionPane.showMessageDialog(this, "Good Job You Got the Right Answer !", "right", JOptionPane.QUESTION_MESSAGE);
                right = true;
                marks++;
            }
        } else if (jRadioButton2.isSelected()) {
            if (jRadioButton2.getText().equals(corret)) {
                JOptionPane.showMessageDialog(this, "Good Job You Got the Right Answer !", "right", JOptionPane.QUESTION_MESSAGE);
                right = true;
                marks++;
            }
        } else if (jRadioButton3.isSelected()) {
            if (jRadioButton3.getText().equals(corret)) {
                JOptionPane.showMessageDialog(this, "Good Job You Got the Right Answer !", "right", JOptionPane.QUESTION_MESSAGE);
                right = true;
                marks++;

            }
        } else if (jRadioButton4.isSelected()) {
            if (jRadioButton4.getText().equals(corret)) {
                JOptionPane.showMessageDialog(this, "Good Job You Got the Right Answer !", "right", JOptionPane.QUESTION_MESSAGE);
                right = true;
                marks++;

            }
        }
        if (right == false) {
            JOptionPane.showMessageDialog(this, "Sorry You got the wrong answer  the Right Answer was " + corret, "wrong", JOptionPane.ERROR_MESSAGE);
        }
        jRadioButton1.setEnabled(false);
        jRadioButton2.setEnabled(false);
        jRadioButton3.setEnabled(false);
        jRadioButton4.setEnabled(false);
        jButton2.setEnabled(false);
        questions.add(jTextField1.getText());    
        jLabel4.setText(String.valueOf(marks + " / " + arr.size()));

    }//GEN-LAST:event_jButton2ActionPerformed

