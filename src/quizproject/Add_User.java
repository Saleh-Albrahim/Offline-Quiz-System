    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
        try {
            String username = nametext.getText();
            MessageDigest digest = MessageDigest.getInstance("SHA-256");
            byte[] hash = digest.digest(jPasswordField1.getText().getBytes(StandardCharsets.UTF_8));
            String encoded = Base64.getEncoder().encodeToString(hash);
            Users newUser = new Users();
            newUser.setUserName(username);
            newUser.setPassword(encoded);
            newUser.setJop(String.valueOf(jComboBox1.getSelectedItem().toString()));
            MainView_Teacher.databaseManager.addUser(newUser);
            nametext.setText("");
            jPasswordField1.setText("");
            jComboBox1.setSelectedItem("");
            refreshTable();
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, "This Username Already Exists", "You can't save", JOptionPane.ERROR_MESSAGE);
            Logger.getLogger(Add_User.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_jButton6ActionPerformed

