    private void deleteBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteBtnActionPerformed
        try {
            int option = JOptionPane.showConfirmDialog(this, "Are sure you want to delete this user", "delete", JOptionPane.WARNING_MESSAGE);
            if (option == 0) {
                MainView_Teacher.databaseManager.removeUser(Cmwrd.getUserName());
            }
        } catch (SQLException ex) {
            Logger.getLogger(Add_User.class.getName()).log(Level.SEVERE, null, ex);
        }
        refreshTable();
    }//GEN-LAST:event_deleteBtnActionPerformed

